    private void buttonHitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHitActionPerformed
        if (gameOver) {
            return;
        }

        int card = drawCard();
        playerTotal += card;

        if (playerTotal >= 21) { // handle 21 as well
            gameOver = true;
            if (playerTotal > 21) {
                labelPlayerTotal.setText("Player Total: " + playerTotal + " (BUST!)");
            } else {
                labelPlayerTotal.setText("Player Total: " + playerTotal + " (WIN!)");
            }
            // Optionally reveal dealer total
            labelDealerTotal.setText("Dealer Total: " + dealerTotal);
        } else {
            updateLabels();
        }

    }//GEN-LAST:event_buttonHitActionPerformed

    private void buttonStandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonStandActionPerformed
        if (gameOver) {
            return;
        }

        // Dealer hits until 17+
        while (dealerTotal < 17) {
            dealerTotal += drawCard();
        }

        gameOver = true;

        // Decide winner
        if (dealerTotal > 21 || playerTotal > dealerTotal) {
            labelPlayerTotal.setText("Player Total: " + playerTotal + " (WIN!)");
        } else if (dealerTotal == playerTotal) {
            labelPlayerTotal.setText("Player Total: " + playerTotal + " (DRAW)");
        } else {
            labelPlayerTotal.setText("Player Total: " + playerTotal + " (LOSE)");
        }

        labelDealerTotal.setText("Dealer Total: " + dealerTotal);


    }//GEN-LAST:event_buttonStandActionPerformed

